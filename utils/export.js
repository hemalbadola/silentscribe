/**
 * SilentScribe — Export Formatters
 * ============================================================================
 * 
 * Pure functions that convert transcript data into various export formats.
 * Each function takes a segments array and session metadata, and returns
 * a formatted string ready for file download.
 * 
 * No side effects, no dependencies beyond constants.js.
 * 
 * Supported formats:
 * - TXT: Plain text with timestamps and speaker labels
 * - SRT: SubRip subtitle format (importable into VLC, Premiere, etc.)
 * - JSON: Full structured data with metadata
 * - MD: Markdown with headers, speakers, and formatted timeline
 * 
 * @module export
 */

import { UI_CONFIG } from './constants.js';


// ============================================================================
// PUBLIC API
// ============================================================================

/**
 * Export transcript as plain text.
 * 
 * Format:
 *   SilentScribe — Meeting Transcript
 *   Date: June 24, 2026 2:30 PM
 *   Platform: Google Meet
 *   Duration: 00:45:12
 *   
 *   [00:00:05] Me: Hello everyone, let's get started.
 *   [00:00:12] Others: Sounds good, let me share my screen.
 * 
 * @param {Object[]} segments - Transcript segments with start, end, speaker, text.
 * @param {Object} session - Session metadata with startTime, platform, duration, speakerNames.
 * @returns {string} Formatted plain text transcript.
 */
export function exportTxt(segments, session) {
  const lines = [];

  // Header
  lines.push('SilentScribe — Meeting Transcript');
  lines.push(`Date: ${formatDate(session.startTime)}`);
  lines.push(`Platform: ${formatPlatformName(session.platform)}`);
  lines.push(`Duration: ${formatDuration(session.duration)}`);
  lines.push('');
  lines.push('─'.repeat(50));
  lines.push('');

  // Segments
  for (const segment of segments) {
    const time = formatTimeBracket(segment.start);
    const speaker = getSpeakerName(segment.speaker, session.speakerNames);
    const text = segment.text.trim();

    lines.push(`${time} ${speaker}: ${text}`);
  }

  lines.push('');
  lines.push('─'.repeat(50));
  lines.push('Generated by SilentScribe');

  return lines.join('\n');
}


/**
 * Export transcript as SRT (SubRip Subtitle) format.
 * 
 * SRT is the universal subtitle format supported by virtually every
 * video player and editor. Each subtitle entry has:
 * - Sequential number (starting from 1)
 * - Timestamp range in HH:MM:SS,mmm --> HH:MM:SS,mmm format
 * - Text content (with optional speaker label prefix)
 * 
 * Format:
 *   1
 *   00:00:05,000 --> 00:00:11,500
 *   [Me] Hello everyone, let's get started.
 *   
 *   2
 *   00:00:12,000 --> 00:01:44,000
 *   [Others] Sounds good, let me share my screen.
 * 
 * @param {Object[]} segments - Transcript segments.
 * @param {Object} session - Session metadata.
 * @returns {string} SRT formatted subtitle file content.
 */
export function exportSrt(segments, session) {
  const entries = [];

  for (let i = 0; i < segments.length; i++) {
    const segment = segments[i];
    const number = i + 1;
    const startTime = formatTimeSrt(segment.start);
    const endTime = formatTimeSrt(segment.end);
    const speaker = getSpeakerName(segment.speaker, session.speakerNames);
    const text = segment.text.trim();

    entries.push(`${number}\n${startTime} --> ${endTime}\n[${speaker}] ${text}`);
  }

  return entries.join('\n\n') + '\n';
}


/**
 * Export transcript as structured JSON.
 * 
 * Includes full session metadata, transcript segments with all fields,
 * and export metadata. Machine-readable format suitable for further
 * processing, analysis, or import into other tools.
 * 
 * @param {Object[]} segments - Transcript segments.
 * @param {Object} session - Session metadata.
 * @returns {string} Pretty-printed JSON string.
 */
export function exportJson(segments, session) {
  const output = {
    session: {
      id: session.id,
      platform: session.platform,
      startTime: session.startTime,
      endTime: session.endTime,
      duration: session.duration,
      micEnabled: session.micEnabled,
      speakerNames: session.speakerNames || {},
    },
    transcript: {
      segmentCount: segments.length,
      wordCount: segments.reduce((sum, s) => sum + countWords(s.text), 0),
      totalDuration: segments.length > 0
        ? segments[segments.length - 1].end - segments[0].start
        : 0,
      segments: segments.map((seg) => ({
        start: round(seg.start, 3),
        end: round(seg.end, 3),
        speaker: getSpeakerName(seg.speaker, session.speakerNames),
        speakerOriginal: seg.speaker,
        text: seg.text.trim(),
        confidence: seg.confidence != null ? round(seg.confidence, 4) : null,
      })),
    },
    exportedAt: new Date().toISOString(),
    version: '1.0.0',
    tool: 'SilentScribe',
  };

  return JSON.stringify(output, null, 2);
}


/**
 * Export transcript as Markdown.
 * 
 * Creates a well-formatted Markdown document with:
 * - Session metadata header
 * - Speaker legend (with custom names if set)
 * - Timestamped transcript with speaker labels in bold
 * 
 * Format:
 *   # Meeting Transcript
 *   
 *   **Date:** June 24, 2026 2:30 PM  
 *   **Platform:** Google Meet  
 *   **Duration:** 00:45:12
 *   
 *   ## Speakers
 *   - **Me** (Rahul)
 *   - **Others** (Priya)
 *   
 *   ## Transcript
 *   
 *   `00:00:05` **Me:** Hello everyone, let's get started.
 *   
 *   `00:00:12` **Others:** Sounds good, let me share my screen.
 * 
 * @param {Object[]} segments - Transcript segments.
 * @param {Object} session - Session metadata.
 * @returns {string} Markdown formatted transcript.
 */
export function exportMd(segments, session) {
  const lines = [];

  // Title
  lines.push('# Meeting Transcript');
  lines.push('');

  // Metadata
  lines.push(`**Date:** ${formatDate(session.startTime)}  `);
  lines.push(`**Platform:** ${formatPlatformName(session.platform)}  `);
  lines.push(`**Duration:** ${formatDuration(session.duration)}`);
  lines.push('');

  // Speaker legend
  const speakerNames = session.speakerNames || {};
  const uniqueSpeakers = [...new Set(segments.map((s) => s.speaker))];
  if (uniqueSpeakers.length > 0) {
    lines.push('## Speakers');
    lines.push('');
    for (const speaker of uniqueSpeakers) {
      const customName = speakerNames[speaker];
      if (customName && customName !== speaker) {
        lines.push(`- **${speaker}** _(${customName})_`);
      } else {
        lines.push(`- **${speaker}**`);
      }
    }
    lines.push('');
  }

  // Transcript
  lines.push('## Transcript');
  lines.push('');

  for (const segment of segments) {
    const time = formatTimeCompact(segment.start);
    const speaker = getSpeakerName(segment.speaker, session.speakerNames);
    const text = segment.text.trim();

    lines.push(`\`${time}\` **${speaker}:** ${text}`);
    lines.push('');
  }

  // Footer
  lines.push('---');
  lines.push(`_Generated by SilentScribe on ${formatDate(Date.now())}_`);

  return lines.join('\n');
}


