// FORMATTING HELPERS
// ============================================================================

/**
 * Format a timestamp in seconds as [MM:SS].
 * Used for plain text export.
 * 
 * @param {number} seconds - Time in seconds.
 * @returns {string} Formatted as [MM:SS].
 */
function formatTimeBracket(seconds) {
  const m = Math.floor(seconds / 60);
  const s = Math.floor(seconds % 60);
  return `[${pad(m)}:${pad(s)}]`;
}


/**
 * Format a timestamp in seconds as MM:SS.
 * Used for Markdown export (without brackets).
 * 
 * @param {number} seconds - Time in seconds.
 * @returns {string} Formatted as MM:SS.
 */
function formatTimeCompact(seconds) {
  const m = Math.floor(seconds / 60);
  const s = Math.floor(seconds % 60);
  return `${pad(m)}:${pad(s)}`;
}


/**
 * Format a timestamp in seconds as HH:MM:SS,mmm for SRT format.
 * 
 * SRT requires the specific format: HH:MM:SS,mmm where mmm is
 * milliseconds (3 digits). Uses comma as the decimal separator
 * (not period — that's the SRT standard).
 * 
 * @param {number} seconds - Time in seconds (may have decimals).
 * @returns {string} Formatted as HH:MM:SS,mmm.
 */
function formatTimeSrt(seconds) {
  const totalMs = Math.round(seconds * 1000);
  const h = Math.floor(totalMs / 3_600_000);
  const m = Math.floor((totalMs % 3_600_000) / 60_000);
  const s = Math.floor((totalMs % 60_000) / 1_000);
  const ms = totalMs % 1_000;

  return `${pad(h)}:${pad(m)}:${pad(s)},${padMs(ms)}`;
}


/**
 * Format a duration in milliseconds as HH:MM:SS.
 * 
 * @param {number} ms - Duration in milliseconds.
 * @returns {string} Formatted as HH:MM:SS.
 */
function formatDuration(ms) {
  if (!ms || ms <= 0) return '00:00:00';

  const totalSeconds = Math.floor(ms / 1000);
  const h = Math.floor(totalSeconds / 3600);
  const m = Math.floor((totalSeconds % 3600) / 60);
  const s = totalSeconds % 60;

  return `${pad(h)}:${pad(m)}:${pad(s)}`;
}


/**
 * Format a Unix timestamp as a human-readable date string.
 * 
 * @param {number} timestamp - Unix timestamp in milliseconds.
 * @returns {string} E.g., "June 24, 2026 2:30 PM".
 */
function formatDate(timestamp) {
  if (!timestamp) return 'Unknown date';

  return new Date(timestamp).toLocaleString('en-US', {
    year: 'numeric',
    month: 'long',
    day: 'numeric',
    hour: 'numeric',
    minute: '2-digit',
    hour12: true,
  });
}


/**
 * Format a platform identifier as a human-readable name.
 * 
 * @param {string} platform - Platform identifier (e.g., 'google-meet').
 * @returns {string} Human-readable name (e.g., 'Google Meet').
 */
function formatPlatformName(platform) {
  const names = {
    'google-meet': 'Google Meet',
    'zoom': 'Zoom',
    'teams': 'Microsoft Teams',
    'webex': 'Cisco Webex',
    'unknown': 'Unknown Platform',
  };
  return names[platform] || platform || 'Unknown Platform';
}


/**
 * Get the display name for a speaker, applying user-defined overrides.
 * 
 * Users can rename speakers post-recording (e.g., "Me" → "Rahul").
 * This function applies those overrides, falling back to the original
 * label if no override exists.
 * 
 * @param {string} speaker - Original speaker label ('Me' or 'Others').
 * @param {Object} speakerNames - Map of original label → custom name.
 * @returns {string} The display name for the speaker.
 */
function getSpeakerName(speaker, speakerNames) {
  if (speakerNames && speakerNames[speaker]) {
    return speakerNames[speaker];
  }
  return speaker || UI_CONFIG.DEFAULT_SPEAKERS.OTHERS;
}


